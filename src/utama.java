    private void tb1DataMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tb1DataMouseClicked
        // TODO add your handling code here:
        int baris = tb1Data.getSelectedRow();
        if (baris !=1) {
            txtNo.setText(tb1Data.getValueAt(baris, 0).toString());
            txtNama.setText(tb1Data.getValueAt(baris, 1).toString());
            txtAlamat.setText(tb1Data.getValueAt(baris, 2).toString());
            if ("pelajaran".equals(tb1Data.getValueAt(baris, 3).toString())){
                rdpelajaran.setSelected(true);
            }else if("komik".equals(tb1Data.getValueAt(baris, 3).toString())){
                rdkomik.setSelected(true);
            }else{
                rdnovel.setSelected(true);
            }            
            txtJudul.setText(tb1Data.getValueAt(baris, 4).toString());
            SimpleDateFormat date = new SimpleDateFormat("yyyy-MM-dd");
            Date dateFormat = null;
            try {
                dateFormat = date.parse(tb1Data.getValueAt(baris, 5).toString());
            } catch (ParseException ex) {
                java.util.logging.Logger.getLogger(utama.class.getName()).log(Level.SEVERE, null, ex);
            }
            Pinjam.setDate(dateFormat);           
            try {
                dateFormat = date.parse(tb1Data.getValueAt(baris, 6).toString());
            } catch (ParseException ex) {
                java.util.logging.Logger.getLogger(utama.class.getName()).log(Level.SEVERE, null, ex);
            }
            Kembali.setDate(dateFormat);
            txtDenda.setText(tb1Data.getValueAt(baris, 7).toString());
            if ("pinjam".equals(tb1Data.getValueAt(baris, 8).toString())){
                rdpinjam.setSelected(true);
            }else{
                rdkembali.setSelected(true);
            }                  
        }
    }//GEN-LAST:event_tb1DataMouseClicked

