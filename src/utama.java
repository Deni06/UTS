    private void btnEditActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditActionPerformed
        // TODO add your handling code here:
        SimpleDateFormat dateFormat = new SimpleDateFormat("yyyy-MM-dd");
        String pinjam = dateFormat.format(Pinjam.getDate());
        String kembali = dateFormat.format(Kembali.getDate());        
        if ("".equals(txtNo.getText()) || "".equals(txtAlamat.getText()) ||
                "".equals(txtJudul.getText()) || "".equals(txtNama.getText()) || "".equals(txtDenda.getText()) || pinjam.equals("") || kembali.equals("")) {
            JOptionPane.showMessageDialog(this, "Harap Lengkapi Data", "Error", JOptionPane.WARNING_MESSAGE);
        }else{
            String JK = "";
            if (rdpinjam.isSelected()) {
                JK = "pinjam";
            }else {
                JK = "kembali";
            }
            String JB = "";
            if (rdpelajaran.isSelected()){
                JB = "Pelajaran";
            }
            else if (rdkomik.isSelected()){
                JB = "Komik";
            }
            else {
                JB = "Novel";
            }
                String SQL = "UPDATE tb_kembali SET "
                + "Nama='" + txtNama.getText()+"', Alamat='"+txtAlamat.getText()+"', " 
                        + "JenisBuku='"+JB+"', JudulBuku='"+txtJudul.getText()+"', TanggalPinjam='"+pinjam+"', TanggalKembali='"+kembali+"', Denda='"+txtDenda.getText()+"', "
                        + "Status='"+JK+"' "
                        + "WHERE NoAnggota='"+txtNo.getText()+"'";
                int status = KoneksiDB.execute(SQL);
                if(status == 1) {
                    JOptionPane.showMessageDialog(this, "Data berhasil update", "Sukses", JOptionPane.INFORMATION_MESSAGE);
                    selectData();
                }else{
                    JOptionPane.showMessageDialog(this, "Data gagal diupdate", "Sukses", JOptionPane.WARNING_MESSAGE);
                }
        }
    }//GEN-LAST:event_btnEditActionPerformed

