    private void selectData() {
        String kolom[] = {"NoAnggota","Nama","Alamat","JenisBuku","JudulBuku","TanggalPinjam","TanggalKembali","Denda","Status"};
        DefaultTableModel dtm = new DefaultTableModel(null, kolom);
        String SQL = "SELECT * FROM tb_kembali";
        ResultSet rs = KoneksiDB.executeQuery(SQL);
        try {
            while(rs.next()) {
                String NoAnggota = rs.getString(1);
                String Nama = rs.getString(2);
                String Alamat = rs.getString(3);
                String JenisBuku = "";
                if ("pelajaran".equals(rs.getString(4))){
                    JenisBuku = "pelajaran";
                }else if("komik".equals(rs.getString(4))){
                    JenisBuku = "komik";
                }else{
                    JenisBuku = "novel";
                }
                String JudulBuku = rs.getString(5);
                String TanggalPinjam = rs.getString(6);
                String TanggalKembali = rs.getString(7);
                String Denda = rs.getString(8);
                String Status = "";
                if ("pinjam".equals(rs.getString(9))) {
                    Status = "pinjam";
                }else{
                    Status = "kembali";
                }                
                String data[] = {NoAnggota,Nama,Alamat,JenisBuku,JudulBuku,TanggalPinjam,TanggalKembali,Denda,Status};
                dtm.addRow(data);
            }   
        } catch (SQLException ex) {
            java.util.logging.Logger.getLogger(utama.class.getName()).log(Level.SEVERE, null, ex);
        }
        tb1Data.setModel(dtm);
    }
